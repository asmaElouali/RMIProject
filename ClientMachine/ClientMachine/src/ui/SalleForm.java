    private void clearFormFields() {
        txtCode.setText("");
    }

    private int getSelectedSalleId() {

        int selectedRow = listeSalles.getSelectedRow(); // Assuming listMachines is your JTable component.

        if (selectedRow != -1) {

            int idColumnIndex = 0;

            Object selectedSalleId = listeSalles.getValueAt(selectedRow, idColumnIndex);

            if (selectedSalleId instanceof Integer) {

                return (Integer) selectedSalleId;
            }

        }

        return -1;
    }

