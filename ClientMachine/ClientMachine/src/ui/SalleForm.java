    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed

        try {

            String Code = txtCode.getText();

            int salleId = getSelectedSalleId();

            Salle deletedSalle = new Salle(Code);
            deletedSalle.setId(salleId);

            if (salleId != -1) {

                boolean deleted = dao1.delete(deletedSalle);

                if (deleted) {
                    JOptionPane.showMessageDialog(this, " Salle a ete Supprimee avec succés!!!");
                    load();
                    clearFormFields();
                } else {

                }
            }
        } catch (RemoteException ex) {
            Logger.getLogger(SalleForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bnDeleteActionPerformed

